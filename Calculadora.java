package aula.qts;

public class Calculadora {
    
    // Classe para somar dois números inteiros.
    public int somar(int a, int b){
        return a + b;
    }

    // Classe para subtrair dois números inteiros.
    public int subtrair(int a, int b){
        return a - b;
    }
    
    // Classe para multiplicar dois números inteiros.
    public int multiplicar(int a, int b){
        return a * b;
    }
    
    // Classe para dividir dois números inteiros.
    public int dividir(int a, int b){
        return a / b;
    }
}
